# Bitwise Operators in Python 
# AND is denoted by '&' and OR is denoted by '|' 
# XOR is denoted by '^' , Bitwise Compliment '~'

a,b=15,10   # a=10 , b=5 

print(a & b)   # 10 = 00001010 & 5=00000101  output-10

print(a | b)  # 10 = 00001010 | 5=00000101  output-15

print( a ^ b) # 10 = 00001010 ^ 5=00000101 output-5
